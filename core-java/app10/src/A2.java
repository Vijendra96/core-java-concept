class A2
{
	public static void main(String[] args)
	{
	System.out.println(95 | 82); // | is bitwise or
	System.out.println(95 & 82); // & is bitwise and
	System.out.println(95 ^ 82); // ^ is bitwise xor
	}
}



/*


95          :	1011111
82          :	1010010
-------------------------
bitwise or	=	1011111 = 95
bitwise and	=	1010010 = 82
bitwise xor	=	0001101 = 13


*/
