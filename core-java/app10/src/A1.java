class A1
{
	public static void main(String[] args)
	{
		System.out.println(25 | 45); // | is bitwise or
		System.out.println(25 & 45); // & is bitwise and
		System.out.println(25 ^ 45); // ^ is bitwise xor
	}
}




/*

25 :			011001
45 :			101101
 --------------------
 bitwise or	 =	111101  = 61
 bitwise and =	001001  =  9
 bitwise xor =	110100  = 52

*/