class C 
{
	public static void main(String[] args) 
	{

		System.out.println("-------------------------------");
		double d = 4526317;
		int i = 25;
		System.out.println(d);		//4526317.0
		System.out.println(i);		//25
		System.out.println(d + i);	//4526342.0
		System.out.println(d - i);	//4526292.0
		System.out.println(d * i);	//1.13157925E8
		System.out.println(d / i);	//181052.68
		System.out.println(d % i);	//17.0
		System.out.println("-------------------------------");
	}
}




